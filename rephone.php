<script>
    //获取长度
    $.ajax({
        type:"get",
        url:"getMysqlLength.php",
        success:function (data) {
            var num=JSON.parse(data).length;
            var navNum=Math.ceil(num/12);//9
            var html="";
            for(var i=0;i<navNum;i++){
                if(i==0){
                    html+='<a  class="active" href="javascript:;">1</a>';
                }else {
                    html+='<a href="javascript:;">'+(i+1)+'</a>';
                }
            }
            $(".nav-num>.no-active:first").after(html);
        }
    });

    var list=$(".product-list");
    //初始加载第一页
    getPage(1);
    //绑定分页按钮点击事件
    $(".nav-num>a:not('.no-active')").live("click",function () {
        var num=$(this).html(); //获取页码
        getPage(num)
    });
    function getPage(num) {
        $.ajax({
            type:"get",
            url:"limit.php",
            data:{num:num},
            success:function (data, status, xhr) {
                var productsList=JSON.parse(data);
                var html="";
                $.each(productsList,function (index, item) {
                    //index 对象在数组中的索引 item 遍历出来的每一个对象
                    var arr=item.src.split(",");
                    if(index%3==2){
                        html+='<li class="third"><a href="detail.php?id='+item.id+'"><img src="'+arr[0]+'" alt=""/><div class="screen-fr"><p class="black">'+item.id+","+item.name+'</p><p class=" sky-blue"> ￥ '+item.price+'</p><p class="sku-item">'+item.attr+'</p></div></a></li>';
                    }else {
                        html+='<li><a href="detail.php?id='+item.id+'"><img src="'+arr[0]+'" alt=""/><div class="screen-fr"><p class="black">'+item.id+","+item.name+'</p><p class=" sky-blue"> ￥ '+item.price+'</p><p class="sku-item">'+item.attr+'</p></div></a></li>';
                    }

                });
                list.html(html);
            }
        });
        $(".nav-num>a").live("click",function () {
            $("body").animate({
                scrollTop:0
            },500)
        })
    }

    //点击下一页
    $(".nav-num>a:last").live("click",function () {
        var nowNum=$(".nav-num>.active").html();
        getPage(parseInt(nowNum)+1);
        $(".nav-num>.active").next().addClass("active").siblings().removeClass("active");
    });
    //点击上一页
    $(".nav-num>a:first").live("click",function () {
        var nowNum=$(".nav-num>.active").html();
        if(nowNum!=1){
            getPage(parseInt(nowNum)-1);
            $(".nav-num>.active").prev().addClass("active").siblings().removeClass("active");
        }

    })


</script>
